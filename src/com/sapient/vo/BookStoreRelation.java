package com.sapient.vo;

public class BookStoreRelation {
	
	private BookStoreKey key;
	
	private Store store;
	
	private Book book;

	public BookStoreKey getKey() {
		return key;
	}

	public void setKey(BookStoreKey key) {
		this.key = key;
	}

	public Store getStore() {
		return store;
	}

	public void setStore(Store store) {
		this.store = store;
	}

	public Book getBook() {
		return book;
	}

	public void setBook(Book book) {
		this.book = book;
	}
	
	
	

}
